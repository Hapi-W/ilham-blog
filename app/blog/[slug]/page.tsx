function renderContent(content: string) {
  const lines = content.trim().split('\n');
  const elements: React.ReactNode[] = [];
  let i = 0;

  while (i < lines.length) {
    const line = lines[i].trim();
    if (line.startsWith('## ')) {
      elements.push(<h2 key={i} className="font-serif text-2xl font-semibold mt-12 mb-4">{line.slice(3)}</h2>);
    } else if (line.startsWith('> ')) {
      elements.push(
        <blockquote key={i} className="border-l-2 border-[#0a0a0a] pl-5 my-8 font-serif italic text-[#555] text-lg leading-relaxed">
          {line.slice(2)}
        </blockquote>
      );
    } else if (line.length > 0) {
      elements.push(<p key={i} className="text-[#333] leading-[1.9] text-[1.05rem] mb-6">{line}</p>);
    }
    i++;
  }
  return elements;
}

